    private void txtdrugCodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtdrugCodeKeyPressed
       
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) 
        {
            try {
                String  drugCode = txtdrugCode.getText();
                prepare = connection.prepareStatement("select * from product where id = ?");
                prepare.setString(1, drugCode);
                rs = prepare.executeQuery();
                
                if(rs.next() == false) 
                {
                    JOptionPane.showMessageDialog(this, "drugCode not found");
                    
                    }
                else 
                {
                    String medname = rs.getString("Name");
                    txtdrugname.setText(medname.trim()) ;
                    
                    String pr = rs.getString("Price");
                    txtdrugprice.setText(pr.trim()) ;
                    
                    txtdrugamount.addNotify();
                    
                    
                    }
                
            } catch (SQLException ex) {
                Logger.getLogger(drugstore.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }
    }//GEN-LAST:event_txtdrugCodeKeyPressed

