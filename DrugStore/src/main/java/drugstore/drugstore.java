    public void expenses()
    {
      
        String totalprice = txtTotalPrice.getText();
        String pay = txtPayment.getText();
        String bal = txtbalance.getText();
         
        int lid = 0;
        
       
        try {
            String query = "insert into expenses(subtotal,amountgiven, balance)values(?,?,?)";
            prepare = connection.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            prepare.setString(1, totalprice);
            prepare.setString(2, pay);
            prepare.setString(3, bal);
            
            prepare.executeUpdate();
            rs = prepare.getGeneratedKeys();
            
            if(rs.next()) 
            {
                lid = rs.getInt(1);
            }
            
            int rows = jTable1.getRowCount();
            String query1 = "insert into productdatabase(saleid,name,price,amount,total)values(?,?,?,?,?)";
            prepare1 = connection.prepareStatement(query1);
            
            String drugname = "";
            String price;
            String amount;
            int total = 0;
            
            for(int i=0; i<jTable1.getRowCount(); i++) 
            {
                drugname = (String)jTable1.getValueAt(i, 0);
                price = (String)jTable1.getValueAt(i, 1);
                amount = (String)jTable1.getValueAt(i, 2);
                total = (int)jTable1.getValueAt(i, 3);
                
                
                prepare1.setInt(1, lid);
                prepare1.setString(2, drugname);
                prepare1.setString(3,price);
                prepare1.setString(4,amount);
                prepare1.setInt(5, total);
                
                prepare1.executeUpdate();
            }
            
            JOptionPane.showMessageDialog(this, "drugs are added successfully");
            
        } catch (SQLException ex) {
            Logger.getLogger(drugstore.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
    
    
    
